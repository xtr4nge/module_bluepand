<b>Blue Pand</b> is a module created by AnguisCaptor to start and stop BlueTooth PAN (Personal Area Network)<br>
Bluetooth Pan is used to share your device internet with FruityWifi, delivering internet without a ethernet cable or by wifi, but instead by Bluetooth
<br><br>
note: use <b>hcitool scan</b> command to get the macaddress.
